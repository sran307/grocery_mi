    public function store(Request $request) {
        $loged = session()->get('user');
        if($loged) {
            $privil = DB::table('previlages as A')
                ->leftjoin('modules as B', 'A.module', '=', 'B.id')
                ->select('A.id as pid','A.*','B.id as mid','B.*')
                ->where('B.module_name', '=', 'CutOff')
                ->where('A.role', '=', $loged->user_type)
                ->where('A.add', '=', 1)
                ->first();

            if($privil) {
            	$page = "Settings";
            	$rules = array(
                    'above_amount'    => 'required|numeric',
                    'shiping_amount'  => 'required|numeric',
                    'remarks'         => 'nullable',
                    'is_block'        => 'nullable',
                );

                $messages=[
                    'cutoff.required'=>'The cutoff field is required.',
                ];
                $validator = Validator::make(Input::all(), $rules,$messages);

                if ($validator->fails()) {
            	   	return View::make('settings.cutoff.add_cutoff')->withErrors($validator)->with(array('page'=>$page));
                } else {
                    $data = Input::all();            
                	$cutoff = new TaxCutoff();

                    if($cutoff) {
        	            $cutoff->above_amount    = $data['above_amount'];	            
        	            $cutoff->shiping_amount  = $data['shiping_amount'];	            
        	            $cutoff->remarks         = $data['remarks'];	            
        	            $cutoff->is_block        = 1;

                        if($cutoff->save()) {
        	                Session::flash('message', 'Add Successfully!'); 
        					Session::flash('alert-class', 'alert-success');
        					return redirect()->route('manage_cutoff');

        	            } else{
        	            	Session::flash('message', 'Added Failed!'); 
        					Session::flash('alert-class', 'alert-danger');
        	                return redirect()->route('manage_cutoff');
        	            }  
                    } else{
                    	Session::flash('message', 'Added Failed!'); 
        				Session::flash('alert-class', 'alert-danger');
                        return redirect()->route('manage_cutoff');
                    }
                }
            } else {
                Session::flash('message', 'You Are Not Access This Module!'); 
                Session::flash('alert-class', 'alert-danger');
                return redirect()->back();
            }
        } else {
            Session::flash('message', 'Please Login Properly!'); 
            Session::flash('alert-class', 'alert-danger');
            return redirect()->back();
        }
    }

