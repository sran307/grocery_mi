                ->first();

            if($privil) {
            	$page = "Settings";
            	$rules = array(
                    'question'   => 'required',
                    'is_block'   => 'nullable',
                );

                $messages=[
                    'question.required'=>'The question field is required.',
                ];
                $validator = Validator::make(Input::all(), $rules,$messages);

                if ($validator->fails()) {
            	   	return View::make('settings.secure.add_secure')->withErrors($validator)->with(array('page'=>$page));
                } else {
                    $data = Input::all();
                	$secure = new loginSecurity();

                    if($secure) {
        	            $secure->question   = $data['question'];	            
        	            $secure->is_block   = 1;

                        if($secure->save()) {
        	                Session::flash('message', 'Add Successfully!'); 
