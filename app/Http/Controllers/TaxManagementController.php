    public function store(Request $request) {
        $loged = session()->get('user');
        if($loged) {
            $privil = DB::table('previlages as A')
                ->leftjoin('modules as B', 'A.module', '=', 'B.id')
                ->select('A.id as pid','A.*','B.id as mid','B.*')
                ->where('B.module_name', '=', 'GST Tax')
                ->where('A.role', '=', $loged->user_type)
                ->where('A.add', '=', 1)
                ->first();

            if($privil) {
            	$page = "Settings";
            	$rules = array(
                    'main_cat_name'  => 'required|exists:category_management_settings,id|unique:tax_managements,main_cat_name',
                    'tax'            => 'required|numeric',
                    'is_block'       => 'nullable',
                );

                $messages=[
                    'main_cat_name.required' =>'The Category Name field is required.',
                    'main_cat_name.unique' =>'The Already Select this Category Name.',
                    'main_cat_name.exists'   =>'The selected Category Name is invalid.',
                ];
                $validator = Validator::make(Input::all(), $rules,$messages);

                if ($validator->fails()) {
                	$main_cat = CategoryManagementSettings::Where('is_block', 1)->get();
            	   	return View::make('settings.tax.add_tax')->with(array('main_cat'=>$main_cat, 'page'=>$page));
                } else {
                    $data = Input::all();
                	$tax = new TaxManagement();

                    if($tax) {
        	            $tax->main_cat_name  = $data['main_cat_name'];	            
        	            $tax->tax            = $data['tax'];	            
        	            $tax->is_block       = 1;

                        if($tax->save()) {
        	                Session::flash('message', 'Add Successfully!'); 
        					Session::flash('alert-class', 'alert-success');
        					return redirect()->route('manage_tax');

        	            } else{
        	            	Session::flash('message', 'Added Failed!'); 
        					Session::flash('alert-class', 'alert-danger');
        	                return redirect()->route('manage_tax');
        	            }  
                    } else{
                    	Session::flash('message', 'Added Failed!'); 
        				Session::flash('alert-class', 'alert-danger');
                        return redirect()->route('manage_tax');
                    }
                }
            } else {
                Session::flash('message', 'You Are Not Access This Module!'); 
                Session::flash('alert-class', 'alert-danger');
                return redirect()->back();
            }
        } else {
            Session::flash('message', 'Please Login Properly!'); 
            Session::flash('alert-class', 'alert-danger');
            return redirect()->back();
        }
    }

