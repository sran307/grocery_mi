    public function store(Request $request) {
        $loged = session()->get('user');
        if($loged) {
            $privil = DB::table('previlages as A')
                ->leftjoin('modules as B', 'A.module', '=', 'B.id')
                ->select('A.id as pid','A.*','B.id as mid','B.*')
                ->where('B.module_name', '=', 'Measurement Units')
                ->where('A.role', '=', $loged->user_type)
                ->where('A.add', '=', 1)
                ->first();

            if($privil) {
            	$page = "Products";
            	$rules = array(
                    'unit_code'       => 'nullable',
                    'unit_name'       => 'required|unique:measurement_units,unit_name',
                    'created_user'    => 'nullable',
                    'modified_user'   => 'nullable',
                    'is_block'        => 'nullable',
                );

                $messages=[
                    'unit_code.required'=>'The Unit Code field is required.',
                ];
                $validator = Validator::make(Input::all(), $rules,$messages);

                if ($validator->fails()) {
            	   	return View::make('products.measurement.add_measurement')->withErrors($validator)->with(array('page'=>$page));
                } else {
                    $data = Input::all();
                    $max = MeasurementUnits::max('unit_code');
                    $max_id = "001";
                	$max_st = "mu";
                    if(($max)) {
                    	$max_no = substr($max, 2);
                    	$increment = (int)$max_no + 1;
                    	$data['unit_code'] = $max_st.sprintf("%03d", $increment);
                    } else {
                    	$data['unit_code'] = $max_st.$max_id;
                    }

                	$measurement = new MeasurementUnits();

                    if($measurement) {
        	            $measurement->unit_code         = $data['unit_code'];	            
        	            $measurement->unit_name         = $data['unit_name'];	            
        	            $measurement->created_user      = 1;	            
        	            // $measurement->modified_user     = $data['modified_user'];	            
        	            $measurement->is_block  = 1;

                        if($measurement->save()) {
        	                Session::flash('message', 'Add Successfully!'); 
        					Session::flash('alert-class', 'alert-success');
        					return redirect()->route('manage_measurement');

        	            } else{
        	            	Session::flash('message', 'Added Failed!'); 
        					Session::flash('alert-class', 'alert-danger');
        	                return redirect()->route('manage_measurement');
        	            }  
                    } else{
                    	Session::flash('message', 'Added Failed!'); 
        				Session::flash('alert-class', 'alert-danger');
                        return redirect()->route('manage_measurement');
                    }
                }
            } else {
                Session::flash('message', 'You Are Not Access This Module!'); 
                Session::flash('alert-class', 'alert-danger');
                return redirect()->back();
            }
        } else {
            Session::flash('message', 'Please Login Properly!'); 
            Session::flash('alert-class', 'alert-danger');
            return redirect()->back();
        }
    }

